#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	int		l;
	char	*dest;

	i = 0;
	l = 0;
	while (src[l])
		l++;
	dest = malloc(sizeof(char) * (l + 1));
	if (dest == NULL)
		return (0);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

